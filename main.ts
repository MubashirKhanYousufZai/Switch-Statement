// Switch Statement
let miniBreak = "Sunday";
switch (miniBreak) {
   case "Monday":
      console.log("Go for a walk");
      break;
   case "sunday":
      console.log("Plan a picnic with family");
      break;
    default:
        console.log("Play a musical instrument");
}

let weather = "sunny";
switch (weather) {
    case "cold":
        console.log("Take hot drinks and food");
        break;
    case "sunny":
        console.log("Avoid sun exposure");
        break;
    default:
        console.log("Wear a rain coat");
}